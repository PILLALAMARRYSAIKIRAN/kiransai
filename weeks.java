class Weeks
{
	public static void main(String args[])
	{
		String day="tuesday";
		String activity="";
		if(day=="monday")
		{
		activity="work";
		}
		else if (day=="tuesday")
		{
		activity="pray";
		}
		else if(day=="wednesday")
		{	
		activity="cricket";
		}
		else if(day=="thursday")
		{
		activity="travelling";
		}
		else if(day=="friday")
		{	
		activity="swimming";
		}
		else if(day=="saturday")
		{	
		activity="movie";
		}
		System.out.println(activity);
		}
}
